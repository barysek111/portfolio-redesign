FILL = "#1A1A1A"


def esc(s: str) -> str:
    return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")


def wrap_text(text: str, max_chars: int) -> list[str]:
    words = text.split()
    lines: list[str] = []
    cur: list[str] = []
    for w in words:
        test = " ".join(cur + [w])
        if len(test) <= max_chars:
            cur.append(w)
        else:
            if cur:
                lines.append(" ".join(cur))
            cur = [w]
    if cur:
        lines.append(" ".join(cur))
    return lines if len(lines) > 1 else [text]


def node_svg(n: dict) -> str:
    x, y, w, h = n["x"], n["y"], n["w"], n["h"]
    cx, cy = x + w / 2, y + h / 2
    if n.get("header"):
        return "\n".join(
            [
                f'<rect x="{x:.3f}" y="{y:.3f}" width="{w:.3f}" height="{h:.3f}" rx="9.482" fill="#FFFFFF" stroke="#D0D0D0" stroke-width="1.724"/>',
                f'<text x="{cx:.3f}" y="{cy + 9:.3f}" text-anchor="middle" class="section-label">{esc(n["text"])}</text>',
            ]
        )
    if "lines" in n:
        lines = n["lines"]
    else:
        max_chars = 14 if w < 90 else 18
        lines = wrap_text(n["text"], max_chars)
    fs = 14.284 if h > 55 else 13.5
    lh = fs * 1.15
    start_y = cy - (len(lines) - 1) * lh / 2 + fs * 0.35
    tspans = "".join(
        f'<tspan x="{cx:.3f}" dy="{lh if i else 0:.3f}">{esc(line)}</tspan>'
        for i, line in enumerate(lines)
    )
    return (
        f'<rect x="{x:.3f}" y="{y:.3f}" width="{w:.3f}" height="{h:.3f}" fill="#FFFFFF"/>'
        f'<text x="{cx:.3f}" y="{start_y:.3f}" text-anchor="middle" class="node-label">{tspans}</text>'
    )


def connector_svg(c: dict) -> str:
    x, y, d = c["x"], c["y"], c["d"]
    return (
        f'<g transform="translate({x:.3f},{y:.3f})">'
        f'<path d="{d}" fill="{FILL}" stroke="none"/>'
        f"</g>"
    )


